export function getModule(key: Exclude<ModuleKey, 'dashboard'>): ModuleDefinition {
  return modules.find((module) => module.key === key) ?? modules[0]
}

function columns(
  definitions: Array<
    [
      name: string,
      type: ColumnSchema['type'],
      readOnly?: boolean,
      referenceTable?: string,
      labelColumns?: string[],
      options?: ColumnSchema['options'],
      required?: boolean,
      format?: string,
      multiple?: boolean,
    ]
  >,
): ColumnSchema[] {
  return definitions.map(([name, type, readOnly = false, referenceTable, labelColumns, options, required, format, multiple]) => ({
    name,
    type,
    nullable: !(required ?? ['id', 'fecha_inicio', 'fecha_fin', 'fecha', 'importe', 'estado'].includes(name)),
    required: required ?? ['fecha_inicio', 'fecha_fin', 'fecha', 'importe', 'estado'].includes(name),
    readOnly: readOnly || commonReadOnly.includes(name),
    format,
    reference: referenceTable
      ? {
          table: referenceTable,
          valueColumn: 'id',
          labelColumns: labelColumns ?? ['nombre'],
        }
      : undefined,
    options,
    multiple,
  }))
}
